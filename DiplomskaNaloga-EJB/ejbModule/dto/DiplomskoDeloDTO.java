package dto;

public class DiplomskoDeloDTO {
	String dijak;
	String datum;
	String profesor;
	String zavod;
	String program;
	String vrstaStudija;
	String naslov;

	public String getDijak() {
		return dijak;
	}

	public void setDijak(String dijak) {
		this.dijak = dijak;
	}

	public String getDatum() {
		return datum;
	}

	public void setDatum(String datum) {
		this.datum = datum;
	}

	public String getProfesor() {
		return profesor;
	}

	public void setProfesor(String profesor) {
		this.profesor = profesor;
	}

	public String getZavod() {
		return zavod;
	}

	public void setZavod(String zavod) {
		this.zavod = zavod;
	}

	public String getProgram() {
		return program;
	}

	public void setProgram(String program) {
		this.program = program;
	}

	public String getVrstaStudija() {
		return vrstaStudija;
	}

	public void setVrstaStudija(String vrstaStudija) {
		this.vrstaStudija = vrstaStudija;
	}

	public String getNaslov() {
		return naslov;
	}

	public void setNaslov(String naslov) {
		this.naslov = naslov;
	}

}
